#cvf: เป็น options ที่นิยมใช้กันมากที่สุดในการสร้างไฟล์ jar
#c ( create ) : สร้างไฟล์ archive (.jar) ใหม่
#v ( verbose ) : แสดงรายละเอียดของไฟล์ที่กำลังถูกเพิ่มเข้าไปใน jar ขณะที่คำสั่งทำงาน (แนะนำให้ใส่ จะได้เห็นว่ามีไฟล์อะไรเข้าไปบ้าง)
#f ( file ) : ระบุว่าเราจะสร้างผลลัพธ์เป็น "ไฟล์" และจะต้องตามด้วยชื่อไฟล์ .jar ที่ต้องการ
#e ( entrypoint ) : ระบุ Main Class ที่จะถูกเรียกใช้เมื่อรัน jar ไฟล์นี้

jar cvfe App.jar com.art.app.Main -C out/ .